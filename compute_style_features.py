"""Compute driving style features from traj.npy and front.npy windows.

This script is a thin wrapper around the ``compute_style_features`` function
from ``build_dataset.py``.  It allows computing the 20-dimensional style
feature vectors for any pre-built window dataset (e.g. rollouts generated by
``generate_policy_rollouts.py``) without having to re-run the full Waymo
TFRecord ingestion pipeline.

Outputs written to --output_dir:
    feat_style_raw.npy        – raw features (float32, shape [N, 20]), may contain NaN
    feat_style.npy            – NaN→0 + global z-score standardised features
    feature_names_style.json  – list of 20 feature name strings

Usage example:
    python compute_style_features.py \\
        --traj_path  output_policy_rollouts/traj.npy \\
        --front_path output_policy_rollouts/front.npy \\
        --output_dir output_policy_rollouts
"""

import argparse
import json
import os
from pathlib import Path

import numpy as np

# Re-use feature computation and names from the existing pipeline.
from style_features_lib import (
    STYLE_FEATURE_NAMES,
    compute_style_features,
)

EPS_DIV_SAFETY = 1e-6


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Compute 20D style features from traj.npy + front.npy windows."
    )
    parser.add_argument(
        "--traj_path",
        type=str,
        default="output/traj.npy",
        help="Path to ego trajectory windows (object or fixed-shape float32 array).",
    )
    parser.add_argument(
        "--front_path",
        type=str,
        default="output/front.npy",
        help="Path to front vehicle trajectory windows.",
    )
    parser.add_argument(
        "--output_dir",
        type=str,
        default=None,
        help="Directory to write feature files; defaults to the directory of --traj_path.",
    )
    parser.add_argument(
        "--min_points_cf",
        type=int,
        default=20,
        help="Minimum car-following mask points to emit CF stats (default 20).",
    )
    parser.add_argument(
        "--kd_min",
        type=float,
        default=1e-3,
        help="Minimum |kd| for desired-gap recovery (default 1e-3).",
    )
    parser.add_argument(
        "--d0_min_gap",
        type=float,
        default=1.0,
        help="Physical lower bound for valid desired-gap values [m] (default 1.0).",
    )
    parser.add_argument(
        "--d0_max_gap",
        type=float,
        default=200.0,
        help="Clip upper bound for desired-gap values [m] (default 200.0).",
    )
    parser.add_argument(
        "--d0_log1p",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Apply log1p compression to desired-gap values (default True).",
    )
    parser.add_argument(
        "--log_every",
        type=int,
        default=500,
        help="Progress log interval (default 500; 0 = silent).",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()

    # ------------------------------------------------------------------
    # Load windows
    # ------------------------------------------------------------------
    print(f"Loading ego trajectories   : {args.traj_path}")
    traj_loaded = np.load(args.traj_path, allow_pickle=True)
    traj_windows = [np.asarray(t, dtype=np.float32) for t in traj_loaded]
    N = len(traj_windows)
    print(f"  {N} windows loaded")

    print(f"Loading front trajectories : {args.front_path}")
    front_loaded = np.load(args.front_path, allow_pickle=True)
    front_windows = [np.asarray(f, dtype=np.float32) for f in front_loaded]
    if len(front_windows) != N:
        raise ValueError(
            f"front length {len(front_windows)} != traj length {N}"
        )

    # ------------------------------------------------------------------
    # Compute style features
    # ------------------------------------------------------------------
    print(f"Computing style features for {N} windows ...")
    feat_raw_list = []
    for i, (ego_win, front_win) in enumerate(zip(traj_windows, front_windows)):
        feat = compute_style_features(
            ego=ego_win,
            front=front_win,
            min_points_cf=args.min_points_cf,
            kd_min=args.kd_min,
            d0_min_gap=args.d0_min_gap,
            d0_max_gap=args.d0_max_gap,
            d0_log1p=args.d0_log1p,
            return_debug=False,
        )
        feat_raw_list.append(feat)
        if args.log_every and (i + 1) % args.log_every == 0:
            print(f"  Processed {i + 1}/{N} windows ...")

    feat_style_raw = np.asarray(feat_raw_list, dtype=np.float32)
    print(f"  Done.  feat_style_raw shape: {feat_style_raw.shape}")

    # Standardise (NaN → 0 then z-score) – same logic as build_dataset.py
    feat_style_raw_filled = np.nan_to_num(
        feat_style_raw, nan=0.0, posinf=0.0, neginf=0.0
    )
    feat_style = (
        feat_style_raw_filled - feat_style_raw_filled.mean(axis=0)
    ) / (feat_style_raw_filled.std(axis=0) + EPS_DIV_SAFETY)
    feat_style = feat_style.astype(np.float32)

    nan_counts = np.isnan(feat_style_raw).sum(axis=0)
    nan_dims = [(STYLE_FEATURE_NAMES[i], int(nan_counts[i])) for i in range(len(STYLE_FEATURE_NAMES)) if nan_counts[i] > 0]
    if nan_dims:
        print(f"  NaN counts per dimension (raw): {nan_dims}")

    # ------------------------------------------------------------------
    # Write outputs
    # ------------------------------------------------------------------
    if args.output_dir is None:
        out_dir = str(Path(args.traj_path).parent)
    else:
        out_dir = args.output_dir
    os.makedirs(out_dir, exist_ok=True)

    raw_path = os.path.join(out_dir, "feat_style_raw.npy")
    std_path = os.path.join(out_dir, "feat_style.npy")
    names_path = os.path.join(out_dir, "feature_names_style.json")

    np.save(raw_path, feat_style_raw)
    np.save(std_path, feat_style)
    with open(names_path, "w", encoding="utf-8") as f:
        json.dump(STYLE_FEATURE_NAMES, f, ensure_ascii=False, indent=2)

    print(f"\nFiles written to: {out_dir}")
    for fpath in [raw_path, std_path, names_path]:
        size_kb = os.path.getsize(fpath) / 1024
        print(f"  {os.path.basename(fpath):<30}  {size_kb:>8.1f} KB")


if __name__ == "__main__":
    main()
